chrome.contextMenus.onClicked.addListener((info, tab) => {
  if (info.menuItemId === 'scan-page') {
    chrome.tabs.sendMessage(tab.id, { action: 'SCAN_PAGE' });
  } else if (info.menuItemId === 'scan-selection') {
    chrome.tabs.sendMessage(tab.id, { action: 'SCAN_SELECTION' });
  } else if (info.menuItemId === 'extract-project') {
    chrome.tabs.sendMessage(tab.id, { action: 'SCAN_PAGE', data: { extractProject: true } });
  } else if (info.menuItemId === 'toggle-live-scan') {
    chrome.tabs.sendMessage(tab.id, { action: 'TOGGLE_LIVE_SCAN', data: { enabled: true } });
  }
});

