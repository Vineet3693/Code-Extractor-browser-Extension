async function handleLiveScanUpdate(data, sender, sendResponse) {
  try {
    const tabId = sender.tab?.id;
    if (!tabId) {
      sendResponse({ success: false, error: 'No tab ID' });
      return;
    }

    if (!liveScanState.has(tabId)) {
      liveScanState.set(tabId, {
        project: null,
        lastUpdate: null,
        updateCount: 0
      });
    }

    const state = liveScanState.get(tabId);
    state.lastUpdate = data;
    state.updateCount++;

    if (data.project) {
      state.project = data.project;
    }

    chrome.runtime.sendMessage({
      action: 'PROJECT_UPDATED',
      data: {
        tabId,
        addedFiles: data.addedFiles,
        updatedFiles: data.updatedFiles,
        conflicts: data.conflicts,
        summary: data.summary,
        timestamp: data.timestamp
      }
    }).catch(() => { });

    sendResponse({ success: true });
  } catch (error) {
    sendResponse({ success: false, error: error.message });
  }
}

async function handleMergeTabs(data, sender, sendResponse) {
  try {
    const allTabs = await chrome.tabs.query({});
    const projects = [];

    for (const tab of allTabs) {
      try {
        const response = await chrome.tabs.sendMessage(tab.id, {
          action: 'TAB_MANAGER_REQUEST_PROJECT'
        });
        if (response?.success && response.project) {
          projects.push({
            tabId: tab.id,
            url: tab.url,
            title: tab.title,
            project: response.project
          });
        }
      } catch (e) { }
    }

    if (projects.length === 0) {
      sendResponse({ success: false, error: 'No projects found in other tabs' });
      return;
    }

    const mergedFiles = [];
    const fileMap = new Map();

    for (const { project, tabId, url } of projects) {
      for (const file of (project.files || [])) {
        const key = (file.path || file.fileName || '').toLowerCase();
        if (!fileMap.has(key)) {
          fileMap.set(key, { ...file, sourceTabs: [{ tabId, url }] });
          mergedFiles.push(fileMap.get(key));
        } else {
          const existing = fileMap.get(key);
          if ((file.timestamp || 0) > (existing.timestamp || 0)) {
            existing.content = file.content;
            existing.size = file.size;
            existing.lines = file.lines;
          }
          existing.sourceTabs.push({ tabId, url });
        }
      }
    }

    const merged = {
      name: 'merged-project',
      files: mergedFiles,
      totalFiles: mergedFiles.length,
      totalLines: mergedFiles.reduce((sum, f) => sum + (f.lines || 0), 0),
      totalSize: mergedFiles.reduce((sum, f) => sum + (f.size || 0), 0),
      mergedFrom: projects.map(p => ({ tabId: p.tabId, url: p.url, title: p.title })),
      mergedAt: Date.now()
    };

    sendResponse({ success: true, merged, sourceTabs: projects.length });
  } catch (error) {
    sendResponse({ success: false, error: error.message });
  }
}

