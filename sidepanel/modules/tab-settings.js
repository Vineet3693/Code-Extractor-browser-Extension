async function loadSettings() {
    try {
        const response = await chrome.runtime.sendMessage({ action: 'GET_SETTINGS' });
        if (response.success) {
            CE.currentSettings = response.data;
            applySettings(CE.currentSettings);
        }
    } catch (error) {
        console.error('Failed to load settings:', error);
    }
}

function applySettings(settings) {
    const setVal = (id, val) => { const el = document.getElementById(id); if (el) el.value = val; };
    const setChk = (id, val) => { const el = document.getElementById(id); if (el) el.checked = val; };

    if (settings.theme) setVal('theme-select', settings.theme);
    if (settings.duplicateStrategy) setVal('duplicate-strategy', settings.duplicateStrategy);
    if (settings.defaultDownloadFormat) setVal('download-format', settings.defaultDownloadFormat);
    if (settings.compressionLevel) setVal('compression-level', settings.compressionLevel);
    if (typeof settings.autoScan === 'boolean') setChk('auto-scan', settings.autoScan);
    if (typeof settings.highlightCodeBlocks === 'boolean') setChk('highlight-blocks', settings.highlightCodeBlocks);
    if (typeof settings.autoGenerateReadme === 'boolean') setChk('include-readme', settings.autoGenerateReadme);
    if (typeof settings.autoGenerateDependencies === 'boolean') setChk('include-dependencies', settings.autoGenerateDependencies);
    if (typeof settings.showNotifications === 'boolean') setChk('show-notifications', settings.showNotifications);

    if (settings.liveScan) {
        if (typeof settings.liveScan.enabled === 'boolean') setChk('live-scan-enabled', settings.liveScan.enabled);
        if (typeof settings.liveScan.autoStart === 'boolean') setChk('live-scan-auto-start', settings.liveScan.autoStart);
        if (typeof settings.liveScan.showNotifications === 'boolean') setChk('live-scan-notifications', settings.liveScan.showNotifications);
        if (typeof settings.liveScan.highlightNewBlocks === 'boolean') setChk('live-scan-highlight', settings.liveScan.highlightNewBlocks);
    }
    if (settings.versionHistory) {
        if (typeof settings.versionHistory.enabled === 'boolean') setChk('version-history-enabled', settings.versionHistory.enabled);
        if (typeof settings.versionHistory.autoSave === 'boolean') setChk('version-history-auto-save', settings.versionHistory.autoSave);
    }
    if (settings.codeValidation) {
        if (typeof settings.codeValidation.enabled === 'boolean') setChk('code-validation-enabled', settings.codeValidation.enabled);
    }
    if (settings.ideExport?.defaultIDE) {
        setVal('default-ide', settings.ideExport.defaultIDE);
    }
}

async function saveSettings() {
    const settings = {
        theme: document.getElementById('theme-select')?.value,
        duplicateStrategy: document.getElementById('duplicate-strategy')?.value,
        defaultDownloadFormat: document.getElementById('download-format')?.value,
        compressionLevel: document.getElementById('compression-level')?.value,
        autoScan: document.getElementById('auto-scan')?.checked,
        highlightCodeBlocks: document.getElementById('highlight-blocks')?.checked,
        autoGenerateReadme: document.getElementById('include-readme')?.checked,
        autoGenerateDependencies: document.getElementById('include-dependencies')?.checked,
        showNotifications: document.getElementById('show-notifications')?.checked,
        liveScan: {
            enabled: document.getElementById('live-scan-enabled')?.checked,
            autoStart: document.getElementById('live-scan-auto-start')?.checked,
            showNotifications: document.getElementById('live-scan-notifications')?.checked,
            highlightNewBlocks: document.getElementById('live-scan-highlight')?.checked
        },
        versionHistory: {
            enabled: document.getElementById('version-history-enabled')?.checked,
            autoSave: document.getElementById('version-history-auto-save')?.checked
        },
        codeValidation: {
            enabled: document.getElementById('code-validation-enabled')?.checked
        },
        ideExport: {
            defaultIDE: document.getElementById('default-ide')?.value
        }
    };

    try {
        await chrome.runtime.sendMessage({ action: 'UPDATE_SETTINGS', data: settings });
        CE.currentSettings = settings;
        showStatus('Settings saved!');
        setTimeout(hideStatus, 2000);
    } catch (error) {
        showError('Failed to save settings');
    }
}

async function exportData() {
    try {
        const response = await chrome.runtime.sendMessage({ action: 'EXPORT_DATA' });
        if (response.success) {
            const blob = new Blob([JSON.stringify(response.data, null, 2)], { type: 'application/json' });
            const url = URL.createObjectURL(blob);
            const a = Object.assign(document.createElement('a'), { href: url, download: `code-extractor-export-${Date.now()}.json` });
            document.body.appendChild(a);
            a.click();
            document.body.removeChild(a);
            URL.revokeObjectURL(url);
            showStatus('Data exported!');
            setTimeout(hideStatus, 2000);
        }
    } catch (error) {
        showError('Failed to export data');
    }
}

async function importData() {
    const input = document.createElement('input');
    input.type = 'file';
    input.accept = '.json';
    input.onchange = async (e) => {
        const file = e.target.files[0];
        if (!file) return;
        try {
            const text = await file.text();
            const data = JSON.parse(text);
            const response = await chrome.runtime.sendMessage({ action: 'IMPORT_DATA', data });
            if (response.success) {
                showStatus('Data imported! Refreshing...');
                setTimeout(() => { hideStatus(); loadSettings(); loadProjects(); }, 2000);
            } else {
                showError(response.error || 'Import failed');
            }
        } catch (error) {
            showError('Invalid file format');
        }
    };
    input.click();
}
